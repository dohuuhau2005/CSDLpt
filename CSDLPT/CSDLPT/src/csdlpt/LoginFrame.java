    private void btnLoginActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnLoginActionPerformed
        // TODO add your handling code here:
        String email = txtAccount.getText().trim();
        String password = new String(txtPass.getPassword()).trim();

        if (email.isEmpty() || password.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Vui lòng nhập email và mật khẩu!");
            return;
        }

        try {
            // 1. Cấu hình URL (Đảm bảo đúng Port server Node.js đang chạy, ví dụ 9999)
                URL url = new URL("http://127.0.0.1:9999/login");            
            HttpURLConnection conn = (HttpURLConnection) url.openConnection();
            conn.setRequestMethod("POST");
            conn.setRequestProperty("Content-Type", "application/json");
conn.setRequestProperty("Charset", "UTF-8");
            conn.setRequestProperty("Accept", "application/json");
            conn.setDoOutput(true);

            String jsonInputString = "{\"email\": \"" + email + "\", \"password\": \"" + password + "\"}";
            // 2. Gửi Request
            try (OutputStream os = conn.getOutputStream()) {
                byte[] input = jsonInputString.getBytes(StandardCharsets.UTF_8);
                os.write(input, 0, input.length);
            }

            // 3. Nhận Response
            int responseCode = conn.getResponseCode();
            StringBuilder response = new StringBuilder();
            
            InputStreamReader reader;
            if (responseCode == 200) {
                reader = new InputStreamReader(conn.getInputStream(), StandardCharsets.UTF_8);
            } else {
                reader = new InputStreamReader(conn.getErrorStream(), StandardCharsets.UTF_8);
            }
            
            try (BufferedReader br = new BufferedReader(reader)) {
                String line;
                while ((line = br.readLine()) != null) {
                    response.append(line.trim());
                }
            }

            String responseBody = response.toString();
            System.out.println("Server Response: " + responseBody);

            // 4. XỬ LÝ PHÂN QUYỀN (LOGIC QUAN TRỌNG NHẤT)
     if (responseCode == 200) {
                String role = extractJsonValue(responseBody, "role");
                String token = extractJsonValue(responseBody, "token");
                
                // BẮT ĐẦU LOGIC LẤY MÃ NV ĐA TẦNG
                String maNV = "";
                
                // Thử lấy key "id" (theo JWT)
                if (maNV.isEmpty()) maNV = extractJsonValue(responseBody, "id");
                
                // Thử lấy key "MaNV" (theo tên cột SQL)
                if (maNV.isEmpty()) maNV = extractJsonValue(responseBody, "MaNV"); 
                
                // Thử lấy key "maNV" (viết thường)
                if (maNV.isEmpty()) maNV = extractJsonValue(responseBody, "maNV"); 
                
                // BỔ SUNG: Kiểm tra cuối cùng, nếu vẫn rỗng, thử key "ID" (phòng trường hợp viết hoa hoàn toàn)
                if (maNV.isEmpty()) maNV = extractJsonValue(responseBody, "ID"); 

                // Lỗi chỉ xảy ra khi maNV bị trích xuất là rỗng.
                if (maNV.isEmpty()) {
                    JOptionPane.showMessageDialog(this, "Đăng nhập thành công, nhưng không lấy được Mã Nhân Viên (ID) để tải dữ liệu.", "Lỗi Dữ liệu", JOptionPane.ERROR_MESSAGE);
                    this.dispose(); 
                    return; 
                }

                // Nếu thành công, tiếp tục mở form
                JOptionPane.showMessageDialog(this, "Đăng nhập thành công! Vai trò: " + role);
                this.dispose(); 

                if (role.equalsIgnoreCase("admin") || role.equalsIgnoreCase("quanly")) {
                    new AdminDashboardFrame(token).setVisible(true);
                } else {
                    // Truyền token và maNV đã lấy được (ví dụ: NV7)
                    new csdlpt.EmployeeAppFrame(token, maNV).setVisible(true);
                }
                
            } else {
                String message = extractJsonValue(responseBody, "message");
                JOptionPane.showMessageDialog(this, "Đăng nhập thất bại: " + message);
            }

        } catch (Exception e) {
            e.printStackTrace();
            JOptionPane.showMessageDialog(this, "Lỗi kết nối Server: " + e.getMessage());
        }
    }//GEN-LAST:event_btnLoginActionPerformed

