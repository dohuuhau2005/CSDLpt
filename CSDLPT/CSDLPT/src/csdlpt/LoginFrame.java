    private String extractJsonValue(String json, String key) {
    if (json == null) return "";
    try {
        String searchKey = "\"" + key + "\":";
        int startIndex = json.indexOf(searchKey);
        if (startIndex == -1) return ""; // Không tìm thấy key

        startIndex += searchKey.length();
        
        // Bỏ qua khoảng trắng
        while (startIndex < json.length() && json.charAt(startIndex) == ' ') {
            startIndex++;
        }
        
        int endIndex;
        boolean isString = false;
        
        // Nếu giá trị bắt đầu bằng dấu ngoặc kép, đây là chuỗi (String)
        if (startIndex < json.length() && json.charAt(startIndex) == '\"') {
            isString = true;
            startIndex++; // Bỏ qua dấu ngoặc kép mở đầu
            endIndex = json.indexOf("\"", startIndex);
        } else {
            // Đây là số, boolean, hoặc null
            endIndex = json.indexOf(",", startIndex);
            if (endIndex == -1) endIndex = json.indexOf("}", startIndex);
            if (endIndex == -1) endIndex = json.length(); // Trường hợp là key cuối cùng
        }

        if (endIndex == -1 || endIndex < startIndex) return ""; // Lỗi cú pháp
        
        String value = json.substring(startIndex, endIndex).trim();
        
        // Nếu nó không phải là chuỗi (isString=false) nhưng lại có dấu ngoặc kép thừa, loại bỏ.
        // Tuy nhiên, với logic mới này, phần lớn dấu ngoặc kép đã được xử lý.
        
        return value;
    } catch (Exception e) {
        System.err.println("Lỗi trích xuất JSON cho key " + key + ": " + e.getMessage());
        return "";
    }
}

